CREATE TABLE activities (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
    reminder_id INTEGER REFERENCES reminders(id) ON DELETE CASCADE NOT NULL,
    medication_id INTEGER REFERENCES medication(id) ON DELETE CASCADE NOT NULL,
    event_id INTEGER REFERENCES event(id) ON DELETE CASCADE NOT NULL,
    notes_id INTEGER REFERENCES notes(id) ON DELETE CASCADE NOT NULL,
    dog_id INTEGER REFERENCES dog_profile(id) ON DELETE CASCADE NOT NULL,
);